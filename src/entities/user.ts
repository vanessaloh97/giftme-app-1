export class User {
  email: string;
  firstName: string;
  lastName: string;
  mobileNumber: string;
  password: string;

  getEmail() {
    return email;
  }

  getFirstName() {
    return firstName;
  }

  getLastName() {
    return lastName;
  }

  getMobileNumber() {
    return mobileNumber;
  }

  getPassword() {
    return password;
  }
}
